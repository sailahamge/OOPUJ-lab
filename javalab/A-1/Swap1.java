public class Swap1
{
	public static void main(String args[])
	{
		int a=10;
		int b=5;
		int temp;
		System.out.println("Before swaping");	
		System.out.println("a and b value"+a+" "+b);
		temp=a;
		a=b;
		b=temp;
		System.out.println("After swaping");	
		System.out.println("a and b value"+a+" "+b);

	
	}
}